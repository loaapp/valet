export const fonts = {
  system: { name: 'System (SF Pro)', value: "-apple-system, BlinkMacSystemFont, 'SF Pro Text', 'Helvetica Neue', sans-serif" },
  inter: { name: 'Inter', value: "'Inter', -apple-system, BlinkMacSystemFont, sans-serif" },
  mono: { name: 'Monospace', value: "'SF Mono', 'Menlo', 'Monaco', monospace" },
};

export function applyTheme(themeId) {
  const theme = themes[themeId];
  if (!theme) return;
  const root = document.documentElement;
  for (const [key, value] of Object.entries(theme.vars)) {
    root.style.setProperty(key, value);
  }
}

export function applyFont(fontId) {
  const font = fonts[fontId];
  if (!font) return;
  document.documentElement.style.setProperty('--font-sans', font.value);
}
